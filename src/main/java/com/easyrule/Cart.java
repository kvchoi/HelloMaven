package com.easyrule;

/**
 * 购物车
 */
public class Cart {

    private int amount;
    private int totalDiscount;

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public int getTotalDiscount() {
        return totalDiscount;
    }
    public void setTotalDiscount(int i) {
        this.totalDiscount = i;
    }

}
